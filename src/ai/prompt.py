"""
AI 프롬프트 (환각 억제 — 설계서 5장)

역할 분담(검증으로 확정):
    - 정확한 사실(마감일·기관·문서번호) = 규칙 추출 (AI 안 씀)
    - 창의적 작업(요약·할일 제안) = AI 가 담당, 단 '근거표시'로 환각 억제

근거표시 이유:
    테스트에서 3b 가 "학업성적 시스템 개선"으로 과장한 적이 있음.
    각 항목에 [근거: 공문에서 해당 부분]을 붙이면 지어낸 것이 즉시 발각됨.
"""

# 공문 본문이 너무 길면 앞부분만 넣습니다 (3b 속도·안정성).
MAX_CHARS = 4000

# 시스템 규칙 (설계서 5장 원문 그대로)
_RULES = """너는 학교 공문을 정리하는 보조도구다. 아래 규칙을 반드시 지켜라.
1. 공문에 없는 내용은 절대 추가하지 마라. 있는 내용만 사용하라.
2. 날짜·기관명·숫자는 공문에 적힌 그대로만 쓰고, 추측하지 마라.
3. 각 요약·할일 항목 끝에 [근거: 공문에서 해당 부분]을 표시하라.
4. 확실하지 않으면 "공문에 명시 안 됨"이라고 쓰고 지어내지 마라."""

# 출력 형식 안내
_FORMAT = """출력 형식(이 형식을 정확히 지켜라):
- 한 줄 요약: (내용) [근거: (공문 해당 부분)]
- 해야 할 일:
  1. (할 일) [근거: (공문 해당 부분)]
  2. (할 일) [근거: (공문 해당 부분)]"""


def build_prompt(text: str, title: str = "") -> str:
    """공문 본문으로 요약·할일 생성 프롬프트를 만듭니다."""
    body = (text or "").strip()
    if len(body) > MAX_CHARS:
        body = body[:MAX_CHARS] + "\n...(이하 생략)"

    title_line = f"공문 제목: {title}\n" if title else ""
    return (
        f"{_RULES}\n\n"
        f"{title_line}"
        f"공문 본문:\n{body}\n\n"
        f"{_FORMAT}\n"
    )
